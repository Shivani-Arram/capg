package day1;

public class Streetlight {

	public static void main(String[] args) {
		String st="red";
		switch(st) {
		case "red":
			System.out.println("Stop");
			break;
		case "orange":
			System.out.println("Ready");
			break;
		case "green":
			System.out.println("Go");
			break;
		}

	}

}
